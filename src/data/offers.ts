const DAY = 24 * 60 * 60 * 1000;

const iso = (offsetDays: number, hour = 8): string => {
  const d = new Date();
  d.setHours(hour, 0, 0, 0);
  d.setTime(d.getTime() + offsetDays * DAY);
  return d.toISOString();
};

const history = (
  currentPrice: number,
  regular: number,
  weeksBack: number,
): PricePoint[] => {
  const points: PricePoint[] = [];
  for (let i = weeksBack; i >= 1; i--) {
    const wobble = (Math.sin(i * 1.7) + 1) * 0.15;
    const dip = i % 3 === 0 ? currentPrice + 0.1 : regular - wobble * 0.2;
    points.push({
      date: new Date(Date.now() - i * 7 * DAY).toISOString(),
      price: Number(Math.max(currentPrice - 0.05, dip).toFixed(2)),
    });
  }
  points.push({ date: new Date().toISOString(), price: currentPrice });
  return points;
};

